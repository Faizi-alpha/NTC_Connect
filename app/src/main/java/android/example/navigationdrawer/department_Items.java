package android.example.navigationdrawer;

public class department_Items {


    String name;
    String designation;
    String email;

    public department_Items(String name,String designation,String email) {
        this.name = name;
        this.designation = designation;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getDesignation() {
        return designation;
    }

    public String getEmail() {
        return email;
    }


}
